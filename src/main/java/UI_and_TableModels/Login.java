    private void signinBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_signinBtnActionPerformed
        if (emailTxt.getText().equals("") && passwordTxt.getText().equals("")) {
            passwordTxt.setBorder(BorderFactory.createLineBorder(Color.red));
        } else if (emailTxt.getText().equals("") || !emailTxt.getText().matches(emailPattern)) {
            emailTxt.setBorder(BorderFactory.createLineBorder(Color.red));

        } else if (passwordTxt.getText().equals("")) {
            passwordTxt.setBorder(BorderFactory.createLineBorder(Color.red));
        } else {
            emailTxt.setBorder(BorderFactory.createLineBorder(Color.WHITE));
            passwordTxt.setBorder(BorderFactory.createLineBorder(Color.WHITE));

            Registrar rgr = null;
            String email = emailTxt.getText();
            String password = passwordTxt.getText();
            rgr = (Registrar) RegistrarDao.login(email, password);
            if (rgr == null) {
                ImageIcon icon = new ImageIcon("src/images/IncorrectPassword.png");
                JOptionPane.showMessageDialog(null, "<html><b style=\"color:red\">Incorrect Password</b></html>", "Message", JOptionPane.ERROR_MESSAGE, icon);
            } else {
                this.setVisible(false);
                /*
                Here we will write the a new admin or home page when the person is created.
                 */
                new MainFrame().setVisible(true);
            }
        }
    }//GEN-LAST:event_signinBtnActionPerformed

